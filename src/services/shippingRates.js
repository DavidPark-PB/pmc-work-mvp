/** 연속 요율표에서 실중량에 해당하는 요금 반환 (가장 가까운 상위 브레이크포인트) */
function lookupStep(table, weightKg) {
  const keys = Object.keys(table).map(Number).sort((a, b) => a - b);
  const bp = keys.find(k => k >= weightKg);
  if (!bp) return null; // 최대 중량 초과
  return table[bp] ?? null;
}

/** KRTHZXR: 무게(g) → 100g 단위 브레이크포인트 → 요금 */
function lookupKRTHZXR(countryCode, weightG) {
  const bp = Math.ceil(weightG / 100) * 100;
  const row = KRTHZXR[Math.min(bp, 2300)];
  return row?.[countryCode] ?? null;
}

/** K-Packet: 무게(g) → 100g 단위 브레이크포인트 → 요금 */
function lookupKPacket(countryCode, weightG) {
  if (weightG > 2000) return null;
  const bp = Math.ceil(weightG / 100) * 100;
  const row = KPACKET_RATES[Math.min(bp, 2000)];
  return row?.[countryCode] ?? null;
}

/** Shipter 유럽: 무게(kg) 가장 가까운 상위 0.1kg 단위 */
function lookupShipter(countryCode, weightKg) {
  const carrier = SHIPTER_COUNTRY_MAP[countryCode];
  if (!carrier) return null;
  const table = SHIPTER_RATES[carrier];
  if (!table) return null;
  return lookupStep(table, Math.round(weightKg * 10) / 10);
}

/** FedEx IE */
function lookupFedExIE(countryCode, weightKg) {
  const zone = FEDEX_ZONE_MAP[countryCode];
  if (!zone) return null;
  const keys = Object.keys(FEDEX_IE_TABLE).map(Number).sort((a, b) => a - b);
  const bp = keys.find(k => k >= weightKg);
  if (!bp) return null;
  return FEDEX_IE_TABLE[bp]?.[zone] ?? null;
}

